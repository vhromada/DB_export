/**
 * An enumeration represents column type.
 *
 * @author Vladimir Hromada
 */
public enum ColumnType {

	/** Boolean */
	BOOLEAN,

	/** Decimal */
	DECIMAL,

	/** String */
	STRING,

	/** Date */
	DATE,

	/** Time */
	TIME,

	/** Timestamp */
	TIMESTAMP,

	/** Object */
	OBJECT

}
